def calculate_spn(zoom_level, object_width_deg, object_height_deg):
    """
    Расчет параметров отображения (spn) карты для объекта по его размеру в градусах.
    В данном случае просто возвращает размеры, умноженные на коэффициент
    """
    # Пример: масштаб пропорционален размерам объекта
    scale_factor = {
        1: 5,
        2: 2.5,
        3: 1.25,
        4: 0.625,
        5: 0.3125,
        6: 0.15625,
        7: 0.078125,
        8: 0.0390625,
        9: 0.01953125,
        10: 0.009765625,
        11: 0.0048828125,
        12: 0.00244140625,
        13: 0.001220703125,
        14: 0.0006103515625,
        15: 0.00030517578125,
        # и так далее
    }
    scale = scale_factor.get(zoom_level, 0.01)
    return object_width_deg * scale, object_height_deg * scale
